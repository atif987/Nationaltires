        // Auto Slider Handler
        if (autoswitch) {
          setInterval(nextSlide, autoswitch_speed);
        }

        // Switch to next slide
        function nextSlide(){
          $('.activated').removeClass('activated').addClass('oldActive');
          if($('.oldActive').is(':last-child')){
            $('.main-slide').first().addClass('activated');
          } else {
            $('.oldActive').next().addClass('activated');
          }
          $('.oldActive').removeClass('oldActive');
          $('.main-slide').fadeOut(speed);
          $('.activated').fadeIn(speed);
        }

        // Switch to prev slide
        function prevSlide(){
          $('.activated').removeClass('activated').addClass('oldActive');
